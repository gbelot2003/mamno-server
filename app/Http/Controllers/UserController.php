<?php

namespace App\Http\Controllers;

use App\Http\Requests\UserRequest;
use App\User;
use Illuminate\Http\Request;

class UserController extends Controller
{
    /**
     * aqui implementamos la seguridad y roles
     * UserController constructor.
     */
    public function __construct()
    {
        // Solo usuarios con rol pueden ingresar a este endpoint
        $this->middleware(['role:Administrador_Sistema'])->except(['configuraciones']);
    }

    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $users = User::with('departamento', 'municipio', 'roles')
            ->orderBy('created_at', 'DESC')->paginate(20);
        return response()->json($users, 200);
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $user = User::where('id', $id)->with('roles')->first();
        return response()->json($user, 200);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(UserRequest $request, $id)
    {
        $user = User::findOrFail($id);
        $user->update($request->all());
        $user->syncRoles([$request->get('role')]);
        return response()->json($user, 200);

    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $user = User::findOrFail($id);
        $user->delete();
        return response()->json('done!', 200);
    }

    /**
     * actualizacion de configuraciones
     * @param Request $request
     * @param $id
     * @return \Illuminate\Http\JsonResponse
     */
    public function configuraciones(Request $request, $id)
    {
        $user = User::findOrFail($id);
        $user->update($request->all());
        return response()->json($user, 200);
    }
}
